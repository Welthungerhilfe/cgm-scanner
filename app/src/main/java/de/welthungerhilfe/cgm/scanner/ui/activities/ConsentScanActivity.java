    /**
     * This a callback object for the {@link ImageReader}. "onImageAvailable" will be called when a
     * still image is ready to be saved.
     */
    private final ImageReader.OnImageAvailableListener mOnImageAvailableListener = new ImageReader.OnImageAvailableListener() {

        @Override
        public void onImageAvailable(ImageReader reader) {
            Image image = reader.acquireLatestImage();

            int width = image.getWidth();
            int height = image.getHeight();

            Image.Plane Y = image.getPlanes()[0];
            Image.Plane U = image.getPlanes()[1];
            Image.Plane V = image.getPlanes()[2];

            int Yb = Y.getBuffer().remaining();
            int Ub = U.getBuffer().remaining();
            int Vb = V.getBuffer().remaining();

            byte[] tmp = new byte[Yb + Ub + Vb];
            Y.getBuffer().get(tmp, 0, Yb);
            byte[] data = tmp.clone();
            U.getBuffer().get(tmp, Yb, Ub);
            V.getBuffer().get(tmp, Yb+ Ub, Vb);

            ByteArrayOutputStream out = new ByteArrayOutputStream();
            YuvImage yuv = new YuvImage(tmp, ImageFormat.NV21, width, height, null);
            yuv.compressToJpeg(new Rect(0, 0, width, height), 100, out);

            Result rawResult;
            try {
                PlanarYUVLuminanceSource source = new PlanarYUVLuminanceSource(data, width, height, 0, 0, width, height, false);
                BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

                rawResult = mQrCodeReader.decode(bitmap);

                qrCode = rawResult.getText();
                imageSaver = new ImageSaver(out.toByteArray());

                runOnUiThread(()->{
                    ConfirmDialog confirmDialog = new ConfirmDialog(ConsentScanActivity.this);
                    String message = getResources().getString(R.string.message_legal) + "\n" + getString(R.string.abbr_qr) + rawResult.getText();
                    confirmDialog.setMessage(message);
                    confirmDialog.show();

                    btnContinue.setVisibility(View.VISIBLE);
                });
            } catch (ReaderException | ArrayIndexOutOfBoundsException e) {
                e.printStackTrace();
                Log.e("QR Result - ", "QR code is not readable");
                runOnUiThread(() -> {
                    txtAlert.setText(R.string.qr_not_readable);
                    txtAlert.setVisibility(View.VISIBLE);
                    btnContinue.setVisibility(View.GONE);
                });
            } finally {
                mQrCodeReader.reset();
                image.close();
            }
        }

    };

